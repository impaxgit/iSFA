package com.example.pauldavies.isfa;

public class A_StockApproved
{
    String productName;
    String qtyOrdered;
    String qtyIssued;
    String qtyRejected;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getQtyOrdered() {
        return qtyOrdered;
    }

    public void setQtyOrdered(String qtyOrdered) {
        this.qtyOrdered = qtyOrdered;
    }

    public String getQtyIssued() {
        return qtyIssued;
    }

    public void setQtyIssued(String qtyIssued) {
        this.qtyIssued = qtyIssued;
    }

    public String getQtyRejected() {
        return qtyRejected;
    }

    public void setQtyRejected(String qtyRejected) {
        this.qtyRejected = qtyRejected;
    }
}
